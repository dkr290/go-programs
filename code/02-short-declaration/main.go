package main

import "fmt"

func main() {

	//short declaration operator
	// DECLARE a variable and ASSIGN a VALUE (of a certain TYPE)
	x := 42
	fmt.Println(x)

	//Reassign the variable with the new values
	x = 99

	fmt.Println(x)

	y := 100 + 24 + x

	fmt.Println(y)

	z := "Bond, James funny"

	fmt.Println(z)
}
